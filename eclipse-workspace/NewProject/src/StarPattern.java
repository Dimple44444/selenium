
public class StarPattern {

	public static void main(String[] args) {
		
		
		 for (int i = 4; i  >= 1; i--) {
	            // Loop to print asterisks in each row
	            for (int j = 1; j  <= i; j++) {
	                System.out.print("*");
	            }
	            // Move to the next line after each row
	            System.out.println();

		
	}
	}

}
